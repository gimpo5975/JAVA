package firstReport;

public class Quiz45 {

	public static void main(String[] args) {
		// 문45) Hello Java 라는 문자열을 3번 반복해서 출력하는 메소드를 작성
		produce(3);
		
		
	}//end of main
	public static void produce(int count) {
		for (int i = 1; i <= count; i++) {
			System.out.println("HELLO JAVA");
		}
	}// 
}//end of class
