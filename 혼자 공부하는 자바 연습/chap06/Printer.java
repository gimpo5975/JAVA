package honggongja.sec06;

public class Printer {
	
	int println(int x) {
		System.out.println(x);
		return x;
	}
	
	boolean println(boolean x) {
		System.out.println(x);
		return x;
	}
	
	double println(double x) {
		System.out.println(x);
		return x;
	}
	
	String println(String message) {
		System.out.println(message);
		return message;
	}
}
