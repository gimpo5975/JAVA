package objectExam01;

public class Cat {

	// 필드
	private String name;

	// 생성자
	public Cat(String name) {
		super();
		this.name = name;
	}// end of constructor

	// getter / setter
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


}// end of class
